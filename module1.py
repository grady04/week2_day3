def area_of_house():
    width = int(input("What is the width of the house in ft? "))
    length = int(input("What is the length of the house in ft? "))
    print(f"{width * length} ft^2")

def circle_circumference():
    radius = int(input("What is the radius of the circle? "))
    print(f"the circumference is {radius * 2 * 3.1415}")